package aula04;

public class app02 {
    public static void main(String[] args) {
        // tipo nome
        // Variáveis do tipo inteiro
        byte numeroPequeno = 127;
        short numeroShort = 32767;
        int numeroMaior = -2147483647;
        long numeroLongo = -9223372036854775807L;
        
        // Variáveis do tipo Ponto flutuantes
        float peso = 78.7f;
        double altura = 1.750089;

        // Variáveis do tipo caracteres
        char letra = 'A';

        // Variáveis do tipo booleano
        boolean verdadeiro = true;
        boolean falso = false;

        // Variáveis tipo String
        String nomeCompleto = "Deyvid Siqueira Miguel";

        System.out.println(numeroPequeno);
        System.out.println(numeroShort);
        System.out.println(numeroMaior);
        System.out.println(numeroLongo);
        System.out.println(peso);
        System.out.println(altura);
        System.out.println(letra);
        System.out.println(verdadeiro);
        System.out.println(falso);
        System.out.println(nomeCompleto);
    }
    
}
